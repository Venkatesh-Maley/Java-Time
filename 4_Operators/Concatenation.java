import java.util.Scanner;

public class Concatenation{
    public static void main(String[] args) {
        System.out.println(2+0+1+6+"Venky");
        System.out.println("Venky"+2+0+1+6);
        System.out.println(2+0+1+5+"Venky"+2+0+1+6);
        System.out.println(2+0+1+5+"Venky"+(2+0+1+6));
    }
    
}