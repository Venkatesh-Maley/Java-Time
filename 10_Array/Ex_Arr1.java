import java.io.*;
class Ex_Arr
{
    public static void main (String[] args)
    {
        //Creating an Integer Array named arr
        int[] arr = new int[10];

        //Accessing Elements at 0 index
        arr[0] = 2;
        arr[1] = 4;
        arr[2] = 6;

        //Access and Print value at 0
        System.out.println(arr[0]);
        System.out.println(arr[1]);
        System.out.println(arr[2]);
    }
}
